float isin(u16 i) {
    i = (i % 360 + 360) % 360;
    if (i < 180) return sine_table[(i < 90) ? i : 180 - i] / 255.0f;
    return -(sine_table[(i < 270) ? i - 180 : 360 - i] / 255.0f);
}

float icos(u16 i) {
    return isin(90-i);
}

